def multi(num1, num2):
    return num1*num2

def sum(num1, num2):
    return num1+num2

num1 = float(input("Enter the first digit: "))
num2 = float(input("Enter the second digit: "))

print("Product of two numbers: ", multi(num1,num2))
print("Sum of two numbers: ", sum(num1,num2))

